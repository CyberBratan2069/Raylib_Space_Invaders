Obstacle* new_obstacle(Vector2 position) {
    obstacle = malloc(sizeof *obstacle);
    if(!obstacle) return NULL;
    obstacle->position = position;

    for(int y=0; y< OBSTACLE_H; y++) {
        for(int x=0; x< OBSTACLE_W; x++) {
            obstacle->grid[y][x] = defaultGrid[y][x];
        }
    }

    return obstacle;
}


void drawObstacle(const Obstacle* obstacle) {
    if(!obstacle) return;
    for(int y=0; y< OBSTACLE_H; y++) {
        for(int x=0; x< OBSTACLE_W; x++) {
            if(obstacle->grid[y][x]) {
                DrawRectangle(obstacle->position.x + x * 3,
                              obstacle->position.y + y * 3,
                              3, 3,
                              (Color){243, 216, 63, 255});
            }
        }
    }
}

